		<h3>Detección </h3>
		<p>La detección de casos sospechosos se puede dar en los servicios de salud (Primer, segundo y tercer nivel de atención) sean estos públicos, seguridad social y privados con y sin fines de lucro, o en puntos de ingreso de viajeros. Verificar estrictamente que cumpla la definición operacional de caso sospechoso de COVID-19.</p>  
		<p>Las personas que se identifiquen como casos sospechosos de COVID-19 deberán separarse de otras personas, se les pondrá una mascarilla quirúrgica y se los ubicará en un área específica para estudio del caso. </p>
		<p>El personal de primer contacto en unidades médicas públicas y privadas de todo el país que identifique un caso sospechoso de COVID-19, deberá llevar a cabo la atención bajo la aplicación de medidas de bioseguridad recomendadas. </p> 
		<p>El médico del establecimiento de salud que atiende el caso al primer contacto con el paciente debe elaborar la historia clínica, realizar el examen físico, considerando los diagnósticos diferenciales, llenado de la ficha epidemiológica y realizar la recolección de las muestras respiratorias según protocolo respectivo, que deben ser referidas de inmediato a los laboratorios autorizados por el Ministerio de Salud en los diferentes Departamentos, en coordinación con los SEDES. </p> 
		<p>Para la toma de decisión se deberá seguir el flujograma recomendado. (Ver anexo Nº 3). </p> 
		<h3>Notificación de casos </h3>
		<p>El 30 de enero de 2020, la OMS declaró el brote por el nuevo Coronavirus en China (enfermedad denominada actualmente COVID-19), como una Emergencia de Salud Pública de Importancia Internacional (ESPII). Por lo tanto, en virtud al Reglamento Sanitario Internacional (RSI) constituye un evento inusitado de notificación obligatoria inmediata.</p> 
		<p>Con el objetivo de fortalecer y coadyuvar en la Vigilancia Epidemiológica de COVID-19 en Bolivia, detectar rápidamente la ocurrencia de casos en nuestro país y proporcionar información epidemiológica, el Sistema Nacional de Información en Salud y Vigilancia Epidemiológica (SNIS-VE) en coordinación con la Unidad de Epidemiología del Ministerio de Salud, habilitó los instrumentos de notificación para casos sospechosos que pudieran llegar a presentarse en Bolivia. </p>
		<h3>Procedimiento de notificación de casos sospechosos </h3>
		<p>En cumplimiento de la gestión y flujo de información establecida por el SNIS-VE, los casos en investigación (sospechosos) de COVID-19, deben ser informados inmediatamente a los responsables de vigilancia epidemiológica del establecimiento a cargo de la atención, que a su vez lo notificarán, de forma urgente a través de la Red de Salud al SNIS-VE tanto departamental (SEDES) como nacional (Ministerio de Salud). </p>
		<p>Para realizar la notificación, el personal de salud deberá completar la ficha establecida (Ver anexo 1), se incluirá el caso en la planilla de notificación inmediata (Anexo B) y en la planilla notificación semanal “Grupo de enfermedades de notificación obligatoria inmediata” al igual que en el formulario 302a del software 2020 del SNIS-VE. </p>
		<p>La notificación inmediata debe realizarse dentro de las 24 horas de ocurrido el caso, utilizando para el envío de la planilla y ficha; el correo electrónico, fax o WhatsApp. </p>
		<p>Para la notificación semanal, la planilla se transmite por correo electrónico o alternativamente WhatsApp y la información se carga en el software específico del Sistema Nacional de Información en Salud y Vigilancia y epidemiológica (formulario 302a).</p> 
		<p>Paralelamente, la notificación por las instancias mencionadas deberá realizarse de forma inmediata a la Unidad de Epidemiologia del Servicio Departamental de Salud (SEDES) correspondiente y a la Unidad de Epidemiología Nacional del Ministerio de Salud. </p>
		<p>Seguidamente, el personal de salud deberá iniciar acciones de control local de acuerdo a los protocolos instituidos por las instancias correspondientes y solicitarán apoyo para realizar la investigación y control respectivo. </p>
		<h3>Variable establecida en las herramientas de consolidación del SNIS-VE para la notificación de casos sospechosos </h3>
		<p>Los casos sospechosos (basados estrictamente en la definición de caso sospechoso establecida para nuestro país) deberán registrase en la variable: COVID-19. Misma que se encontrará en las herramientas de notificación establecidas por el SNIS-VE. </p>
		<p>En los sistemas informáticos SOAPS y SICE, los casos que cumplan con la definición de caso sospechoso deben ser registrados bajo el código CIE 10 “U07.1”, establecido por la Red Latinoamericana y del Caribe para el Fortalecimiento de los Sistemas de Información de Salud (RELACSIS) para estos casos.</p> 
		<h3>Definiciones de caso </h3>
		<p>La información para reportarse en las herramientas del SNIS-VE (planilla de notificación inmediata, semanal y el formulario 302a), corresponde a los casos sospechosos. </p>
		<h3>Caso Sospechoso </h3>
		<p>A.	Un paciente con INFECCIÓN RESPIRATORIA AGUDA (Fiebre y al menos un signo/síntoma de enfermedad respiratoria, por ejemplo: tos, dificultad para respirar), Y sin otra etiología que explique completamente la presentación clínica Y un historial de viaje o residencia en un PAÍS/ÁREA O TERRITORIO QUE REPORTÓ LA TRANSMISIÓN LOCAL DE LA ENFERMEDAD COVID-19 durante los 14 días anteriores a la aparición de síntomas; O </p>
 
		<p>B.	Un paciente con alguna INFECCIÓN RESPIRATORIA AGUDA Y que haya estado en contacto con un caso COVID-19 confirmado en los últimos 14 días antes del inicio de los síntomas: O</p> 
		 
		<p>C.	Un paciente con INFECCIÓN RESPIRATORIA AGUDA GRAVE (fiebre y al menos un signo/síntoma de enfermedad respiratoria (por ejemplo, tos, dificultad para respirar) Y que requiere hospitalización Y sin otra etiología que explique completamente la presentación clínica.  </p>
		<h3>Caso Confirmado </h3>
		<p>Caso sospechoso con prueba de laboratorio RT-PCR para SARS-CoV-2 Positiva.</p>
		<h3>Caso Descartado </h3>
		<p>Caso sospechoso con prueba de laboratorio RT-PCR para SARS-CoV-2 Negativa. </p>
		<h3>Caso Probable </h3>
		<p>▪	Un caso sospechoso para quien las pruebas para el virus COVID-19 no son concluyentes, o </p>
		<p>▪	Un caso sospechoso para quien las pruebas no pudieron realizarse por ningún motivo. 
		</p>
		<h3>Caso Recuperado </h3>
		<p>▪	Caso confirmado que después de la remisión del cuadro clínico inicial presenta 2 pruebas negativas de RT-PCR para SARS-CoV-2, con intervalo mínimo de 24 horas entre ambas pruebas (Anexo N°32). </p>
		<h3>Investigación </h3>
		<p>La investigación de caso debe iniciarse inmediatamente notificado el caso por el médico, verificando que SI corresponde a la definición de caso sospechoso (sea de atención hospitalaria o en domicilio).  </p>
		<p>El responsable de Epidemiología designado debe verificar que: </p>
		<ul>
			<li>	Sí corresponde a la definición de caso sospechoso. </li>
			<li>	Se haya recolectado las muestras respiratorias. </li>
			<li>	Se realice la búsqueda de contactos. </li>
			<li>	Se establezca las acciones de prevención y control de acuerdo al contexto local y situación específica.  </li>
		</ul>
		<h3>Atención de brotes </h3>
		<p>Ante la detección de un caso de COVID-19, en un establecimiento de salud, se debe investigar la procedencia de este (nexo epidemiológico). Es necesario realizar la investigación de contactos (listado, seguimiento diario, educación, recomendaciones y signos de alarma) por parte del equipo local de vigilancia epidemiológica de acuerdo con protocolo. </p>
		<h3>Definición de Contactos </h3>
		<p>Un contacto es una persona que experimentó cualquiera de las siguientes exposiciones durante los dos días anteriores y 14 días después del inicio de los síntomas de un caso probable o confirmado:  </p>
		
		<p>1.	Contacto cara a cara con un caso probable o confirmado a un metro de distancia o menos y durante más de 15 minutos;</p>  
		<p>2.	Contacto físico directo con un caso probable o confirmado; </p> 
		<p>3.	Atención directa a un paciente probable o confirmada con enfermedad COVID-19 sin usar el equipo de protección personal adecuado; O  <>/p
		<p>4.	Otras situaciones según lo indicado por las evaluaciones de riesgos locales. </p>
		<p>Para casos asintomáticos confirmados, el período de contacto se considera desde dos días anteriores hasta 14 días posteriores a la fecha en que se tomó la muestra que condujo a la confirmación. </p>
		<p>Ante la identificación de casos sospechosos se deberá proceder a la identificación y seguimiento de la totalidad de los contactos durante los 14 días posteriores al inicio de síntomas del caso. </p>
		<h3>Estudio y manejo de contactos: </h3>
		<p>En personas asintomáticas que cumplan la definición de contacto deberán ser informadas y se iniciará los procedimientos de la vigilancia epidemiológica respectivos, explicándoles las precauciones de higiene respiratoria, etiqueta de la tos y lavado de manos. </p>
		<p>Se deberá REALIZAR UNA VALORACIÓN INDIVIDUALIZADA DE CADA SITUACIÓN, adaptando las recomendaciones a cada caso específico teniendo en cuenta el riesgo real de exposición. Todos los contactos, de casos confirmados por laboratorio, deben permanecer en cuarentena durante 14 días desde la última vez que estuvieron expuestos a un paciente con COVID-19.  No se recomienda la toma de muestra de rutina de los contactos. </p> 
		<p>Si durante los 14 días posteriores a la exposición desarrollara síntomas, deberá hacer autoaislamiento inmediato domiciliario y contactar de forma urgente con el responsable que se haya establecido para su seguimiento dentro del contexto y manejo de caso sospechoso.  </p>
		<p>El personal de salud les indicará dónde deben acudir si uno de los contactos presenta síntomas, qué medio de transporte es el más adecuado, cuándo y por dónde deben entrar al centro médico señalado y qué precauciones de control de la infección deben seguir: </p>
		<ul>
			<li>	Notificar al centro de salud señalado que un contacto sintomático se dirige a sus instalaciones. </li> 
 
			<li>	Durante el desplazamiento, el enfermo debe llevar mascarilla quirúrgica. </li>
			 
			<li>	Evitar el transporte público en la medida de lo posible; llamar a una ambulancia o trasladar al enfermo en un vehículo privado, con las ventanillas abiertas si es posible. </li> 
			 
			<li>	Debe indicarse al enfermo que siga en todo momento las medidas de higiene respiratoria y de manos y que se mantenga lo más alejado que pueda de las demás personas (al menos un metro de distancia), ya sea sentado o de pie, durante el desplazamiento y dentro del centro de salud.  </li>
			 
			<li>	Tanto el contacto como los cuidadores deben seguir las medidas adecuadas de higiene de manos. (Anexo Nº 9.) </li>
			 
			<li>	Las superficies que se manchen con secreciones respiratorias o fluidos corporales durante el desplazamiento habrán de limpiarse y desinfectarse con desinfectante corriente de hogar, compuesto por una solución de lavandina al 1% y 99% de agua. </li>
			
		</ul>
		<p><b>Toma y transporte de muestras de casos sospechosos </b></p>
		<p>Todas las muestras tomadas para detección por laboratorio de virus respiratorios deben ser manejadas como potencialmente infecciosas, y el personal que tome o transporte muestras clínicas debe cumplir con las guías nacionales del Protocolo Nacional de Vigilancia de Influenza y otros virus respiratorios. </p>
		<p>La evidencia actual sugiere que las muestras de laboratorio de SARS-CoV-2 pueden ser manejadas como se procede para muestras sospechosas por virus Influenza.  </p>
		<p>Las pruebas para el SARS-CoV-2 deben considerarse únicamente para pacientes que cumplan la definición de caso sospechoso. </p>

