		<p>Para los pacientes que cumplan con la definición de caso sospechoso de COVID-19, se debe solicitar pruebas de laboratorio para el diagnóstico respectivo. Siendo la prueba de RT-PCR la indicada para el diagnóstico de COVID-19 así como para definir la condición de CURADO de la enfermedad. </p>
		<p>Otro tipo de pruebas (serología IgM e IgG) evalúan la seroconversión del paciente frente a SARS–CoV–2, y su uso e indicación se considerarán de acuerdo a su sensibilidad y especificidad según el tiempo transcurrido desde el inicio de los síntomas, considerando que la presencia de anticuerpos frente a COVID-19, es detectable recién a partir del 7mo. día la enfermedad, por lo cual antes de este tiempo la probabilidad de falsos negativos es muy alta.  </p>
