<button class="btn myButton" type="button">
	<span class="glyphicon glyphicon-chevron-right"></span> Toma de Muestras RT-PCR: COVID-9 
</button>
<div class="collapse" id="collapseExample1">
	<div class="well">
		<p>El personal que tome la muestra debe cumplir los protocolos de bioseguridad y cuidados básicos para este procedimiento.  </p>
		<p><b>Procedimiento para toma de muestra </b></p>
		<p>Antes de tomar las muestras el profesional debe tener listo y a mano todo el material necesario para el procedimiento. </p>
		<p><b>a) Debe utilizar: </b></p>
		<ul>
			<li> Bata de laboratorio desechable. </li>
			<li> Protector respiratorio (N95). </li>
			<li> Guantes de nitrilo (en caso de no tener los de nitrilo, puede usar doble guante de látex). </li>
			<li> Gafas protectoras con cubierta lateral superior e inferior (antes y después de uso, estas deben ser desinfectadas con alcohol al 70%). </li>
		</ul>
		<p>El laboratorio debe contar la siguiente información: </p>
		<br>
		<p><b>b) Datos del paciente  </b></p>
		<ul>
			<li> Nombre el paciente.  </li>
			<li> Sexo. </li>
			<li> Dirección.</li>
			<li> Nombre del establecimiento de salud que remitió la muestra. </li>
			<li> Número de habitación. </li>
			<li> Nombre del médico tratante. </li>
			<li> Datos de contacto. </li>
		</ul>
		<br>
		<p><b>c) Información de la toma de muestra  </b></p>
		<ul>
			<li> Fecha y hora de recolección de muestras.   </li>
			<li> Sitio anatómico y ubicación de la colección de muestras.  </li>
			<li> Pruebas solicitadas.</li>
			<li> Síntomas clínicos y antecedentes relevantes del paciente. (Incluyendo vacunación, tratamientos anteriores, información epidemiológica, y factores de riesgo).  </li>
		</ul>
		<br>
		<p><b>d) Toma de muestra  </b></p>
		<p>Las muestras requeridas para el diagnóstico de SARS-CoV-2 son:  </p>
		<ul>
			<li> Muestras recolectadas del tracto respiratorio inferior, incluidas el esputo, el lavado bronco alveolar.    </li>
			<li> Aspirado traqueal (cuando sea posible según los criterios médicos). </li>
			<li> Sin embargo, cuando la toma de una muestra del tracto respiratorio inferior no es posible, las muestras del tracto respiratorio superior también son útiles (hisopados nasofaríngeo y/o orofaríngeo). </li>
		</ul>
		<br>
		<h3>Conservación de la muestra </h3>
		<ul>
			<li>Lavado broncoalveolar y aspirado traqueal: Recoja 2-3ml., en un recipiente estéril. Refrigere la muestra a 2-8°C hasta enviar al laboratorio.  </li>
			<li>Esputo: Haga que el paciente se enjuague la boca con agua y luego expectore directamente en un recipiente estéril. Refrigere la muestra a 2-8°C hasta enviar al laboratorio.  </li>
		</ul>
		<p>El profesional que realice la toma de muestras debe portar todos los elementos de protección personal descritos. Verificar de tener a mano todos los insumos necesarios para la recolección de las muestras (hisopos de dacron, es importante no utilizar hisopos con mango de madera o cabeza de algodón ya que pueden interferir en las pruebas de PCR) medio de transporte viral, material para identificar las muestras). </p>
		<p><b>Procedimiento. </b></p>
		<ul>
			<li>Registre el tubo o los tubos con el nombre del paciente. </li>
			<li>Lávese las manos antes de colocar los elementos de protección personal. </li>
			<li>Realice la pruebe de ajuste al protector respiratorio. </li>
			<li>Solicite al paciente que abra la boca. </li>
			<li>Sujete la lengua del paciente con depresor de lengua. </li>
			<li>Introduzca el hisopo con firmeza hasta la pared posterior de la garganta (orofaringe) frote con movimientos circulares para obtener células infectadas, evite tocar la úvula para no provocar vomito al paciente (ver figura Nº 12). </li>
			<li>Una vez recolectada la muestra Introduzca el hisopo en el tubo que contiene el medio de transporte viral. </li>
			<li>Cerrar el tubo perfectamente y mantenerlo de 2 a 8°C hasta su recepción en el laboratorio. </li>
			<li>Lavase las manos antes de retirarse del área donde se tomó la muestra </li>
			<li>Lavase las manos antes de retirar todos los elementos utilizados en la toma de muestras. </li>
			
		</ul>
		<br>
		<center><p>Figura Nº 12. Toma de muestra de exudado faríngeo </p></center>
		<img src="<?= Yii::$app->request->BaseUrl ?>/images/9_img1.jpg"  alt="">
		<br>
		<p><b>Exudado nasofaríngeo </b></p>
		<p>La forma adecuada para tomarlo y obtener una buena muestra es la siguiente: </p>
		<ul>
			<li>	Registre el tubo o los tubos con el nombre del paciente. </li> 
			<li>	Higienizar las manos antes de colocar los elementos de protección personal.  </li>
			<li>	Realice la prueba de ajuste al protector respiratorio.  </li>
			<li>	Recostar al paciente y elevar un poco su cabeza. </li>
			<li>	Introducir por una fosa nasal, suavemente el hisopo, paralelo al paladar, casi en su totalidad hasta llegar a la nasofaringe (aproximadamente 2.5 cm en adultos y un poco menos en niños). Ver figura Nº 13. </li>
			<li>	Rotar el hisopo suavemente para frotar la pared de la nasofaringe (al frotar se obtienen células infectadas por el virus). </li>
			<li>	Retirar cuidadosamente sin dejar de rotar. </li>
			<li>	Introducir el hisopo en el tubo que contiene el medio de transporte viral. </li>
			<li>	Cerrar el tubo perfectamente y mantenerlo de 2 a 8°C.</li> 
		</ul>
		<br>
		<center><b>Figura Nº 13. Toma de exudado nasofaríngeo </b></center>
		<img src="<?= Yii::$app->request->BaseUrl ?>/images/9_img2.jpg"  alt="">
		<center><p>Imagen referencial de: Clinical Microbiology Department, Ramon y Cajal Hospital. Madrid.</p></center>
		<h3>Conservación, embalaje y transporte</h3>
		<p>Las muestras deben mantenerse refrigeradas (4-8°C) y enviarse al laboratorio donde se procesarán dentro de las 24-72 horas de la toma. Si no se pueden enviar muestras dentro de este período, se recomienda congelarlas a -70°C (o menos) hasta que se envíen (asegurando que se mantenga la cadena de frío).</p>
		<p>El transporte de las muestras debe realizarse con geles o pilas congeladas, temperaturas superiores a 8°C degradan la partícula viral, obteniéndose falsos negativos. Muestras que lleguen al laboratorio con temperaturas superiores a 8°C, no serán procesadas.</p>
		<p>Para el transporte de muestras debe usarse el sistema básico de Triple Empaque. De acuerdo a las normativas vigentes para el transporte seguro de sustancias infecciosas.</p>
		<p>El embalaje debe constar de tres componentes:</p>
		<p><b>A)</b> Recipiente principal o primario hermético: es el que contiene la muestra</p>
		<p><b>B)</b> Embalaje secundario/contenedor secundario hermético: debe ser resistente y anti fugas</p>
		<p><b>C)</b> Embalaje exterior rígido adecuado: cuenta con una superficie de una dimensión de al
		menos 10 cm x 10 cm.</p>
		<br>
		
		<center><p>Figura Nº 14. Alternativas de triple empaque</p></center>
