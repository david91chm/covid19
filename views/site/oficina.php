<button class="btn myButton" type="button">
	<span class="glyphicon glyphicon-chevron-right"></span> Regular al paciente que llama
</button>
<div class="collapse" id="collapseExample">
	<div class="well">
		<p>Si la oficina tiene una secretaría, regule las llamadas.</p>
		<ul>
			<li>Intervalos de consultas dedicados a pacientes que consultan por signos respiratorios en un momento diferente de las consultas por otros motivos (final del día).</li>
			<li>Monitoreo de patología crónica estable: teleconsulta y / o renovación del tratamiento por la farmacia por 1 mes adicional</li>
			<li>Sugerir una teleconsulta. Objetivo: definir la necesidad de tratamiento (consulta física en medicina general, consulta externa especializada, traslado al hospital) o una ingesta</li>
		</ul>
	</div>
</div>

<button class="btn myButton" type="button">
	<span class="glyphicon glyphicon-chevron-right"></span> En la sala de espera
</button>
<div class="collapse" id="collapseExample">
	<div class="well">
		<p>Adapte la sala de espera:</p>
		<ul>
			<li>Separe todas las sillas a un metro de distancia.</li>
			<li>Eliminar todas las revistas, volantes y juguetes.</li>
			<li>Proporcione las áreas de espera tanto como sea posible donde los sospechosos de Covid-19 puedan ser aislados.</li>
			<li style="color:red;">Un paciente que ingresa a la sala de espera con síntomas respiratorios altos o bajos, con o sin fiebre, se pone una máscara y se le pide que se lave las manos si es posible.</li>
			<li>Respeto a una distancia de 1 metro con el personal de recepción o dispositivos de protección para el personal de recepción (vidrio o plexiglás en el área de recepción).</li>
			<li>A menos que sea imposible, el paciente no debe ser acompañado para hacer su consulta. Si es posible, una sala de consulta para pacientes potencialmente infectados con COVID-19.</li>
		</ul>
	</div>
</div>

<button class="btn myButton" type="button">
	<span class="glyphicon glyphicon-chevron-right"></span> Paciente potencialmente COVID-19
</button>
<div class="collapse" id="collapseExample">
	<div class="well">
		<ul>
			<li>Máscara quirúrgica</li>
			<li>Durante el cuidado cercano, gire la cabeza.</li>
			<li>Si es posible, la blusa se cambia todos los días (lavar todas las noches a 60º). Si no, ropa específica al llegar a la oficina, quitada antes de regresar a casa</li>
			<li>Sin joyas (anillo de bodas, reloj), antebrazos sueltos, cabello atado</li>
			<li>Limpiar y desinfectar superficies y dispositivos médicos (estetoscopio, saturómetro, termómetro) , monitor de presión arterial ...)</li>
			<li>Lavado de manos después de manipular la tarjeta bancaria y el lector de tarjeta vital.</li>
			<li>Deseche los residuos y elementos de protección de acuerdo con el método habitual.</li>
			<li>Limpie las manijas de las puertas con regularidad y cierre los baños públicos o desinféctelos si el paciente potencialmente infectado COVID-19 los ha usado.</li>
		</ul>
	</div>
</div>

<button class="btn myButton" type="button">
	<span class="glyphicon glyphicon-chevron-right"></span> Criterios de vulnerabilidad
</button>
<div class="collapse" id="collapseExample">
	<div class="well">
		<ul>
			<li>Mujeres embarazadas</li>
			<li>Enfermedades respiratorias crónicas (asma, bronquitis crónica ...);</li>
			<li>Insuficiencia respiratoria crónica;</li>
			<li>fibrosis quística;</li>
			<li>Insuficiencia cardíaca por cualquier causa;</li>
			<li>Enfermedades coronarias;</li>
			<li>Historia de accidente cerebrovascular;</li>
			<li>Hipertensión arterial ;</li>
			<li>Enfermedad renal crónica en diálisis;</li>
			<li>Diabetes insulinodependiente tipo 1 y diabetes tipo 2;</li>
			<li>Personas con inmunosupresión:
				<ul>
					<li>Cáncer y patologías hematológicas, trasplantes de órganos y células madre hematopoyéticas</li>
					<li>Enfermedades inflamatorias y / o autoinmunes que reciben terapia inmunosupresora,</li>
					<li>Personas con Enfermedad hepática crónica</li>
				</ul>
			</li>
			<li>Infectada por VIH</li>
			<li>Con cirrosis;</li>
			<li>Obesidad con un índice de masa corporal (IMC) igual o superior a 40.</li>
		</ul>
		<p>De acuerdo con las decisiones del gobierno, estas personas deben permanecer en su hogar, cuando están fuera del trabajo, si no es posible una solución de teletrabajo.</p>
	</div>
</div>

